    <td class="text">
<span class="text">
dorkbot.swiss ist  ein alle zwei monate stattfindendes meeting von k&uuml;nstlern [sound/image/movement/etc tec, etc...], designern, ingeneuren, studenten und anderen interessierten , die in schaffung und gestaltung elektronischer kunst ( im weitm&ouml;glichsten sinne) involviert sind. 
jedes dorkbot.swiss meeting pr&auml;sentiert zwei nationale kreative und einen internationalen gast. durch den internationalen gast wollen wir nicht nur den internationalen austausch, sondern auch den nationalen f&ouml;rdern, indem wir den anreiz zu einem meeting in einer "anderen" stadt zu fahren erh&ouml;hen.
</span>
<p class="text">
<span class="title">die absicht von dorkbot.swiss ist es:</span>
<ul class="list">
	<li>den k&uuml;nstlern/programmierern/ingeneuern die m&ouml;glichkeit zu bieten, ihre arbeit 	anderen k&uuml;nstlern verschiedenster richtungen vorzustellen und mit ihnen zu 	diskutieren und zu betrachten.</li> 
	<li>ein forum f&uuml;r die pr&auml;sentation neuer art works/ technologien/ software/ hardware zu 	etablieren</li>
	<li>hilfestellung zu leisten bei der vermittlung von kontakten, beziehungen und 	kollaborationen zu f&ouml;rdern zwischen k&uuml;nstlern aus den unterschiedlichsten bereichen 	mit unterschiedlichsten interessen.</li>
	<li>k&uuml;nstler anzusprechen, denen ansonsten eine plattform fehlt, ihre arbeiten zu 	pr&auml;sentieren und somit der erweiterung und der entwicklung neuer 	pr&auml;sentationsformen verhelfen</li>
	<li>uns allen die m&ouml;glichkeit zu geben, all die spannenden sachen zu sehen, die unsere 	nachbarn so erfinden und entwickeln</li>
</ul>
</p>
<p class="text">
<span class="title">vorstellbare pr&auml;sentations themen:</span>
<ul class="list">
	<li>ein demo einer neuen motion tracking software, die man gerade schreibt</li>
	<li>ein video zu zeigen, dass man gemacht hat</li>
	<li>eine anleitung und erkl&auml;rung, wie man seinen game boy gehacked hat, um ihn dazu zu 	bringen schr&auml;ge piepsende ger&auml;usche zu machen</li>
	<li>trouble shooting/ brainstorming eines ferngesteuerten sandwich machenden robots
	<li>man bringt, die t&auml;nzer mit denen man arbeitet mit, die dann das interface -	demonstrieren in einem neuen mind control ballett</li>
	<li>diskussion &uuml;ber deinen zugang zu elektronischer bildhauerei</li>
	<li>etc...</li>
</ul>
</p>
<p class="text">
demos kommerzieller software sind nicht geeignet, ausser man hat die software selbst
entwickelt oder man verwendet sie in einem total neuen zusammenhang.
hingegen ist die pr&auml;sentation von arbeiten, die man mit hilfe kommerzieller software kreiert hat, willkommen.
dorkbot meetings sind grunds&auml;tzlich eintritt frei und f&uuml;r jeden zug&auml;nglich!
</p>
<p class="text">
<span class="title">wann/wo:</span>
dorkbot.swiss meetings finden alle zwei monate rotierend in luzern, z&uuml;rich, basel und bern statt.
</p>
<p class="text">
<span class="title">meeting-format:</span> 
dorkbot.swiss meetings sind weitgehend informell und zwanglos.
erleichternde rahmenstruktur bislang:
<ul class="list">
	<li>kurze intros, ank&uuml;ndigungen, administrative details</li>
	<li>20-30 min pr&auml;sentation, danach ca. 10 min talk (frage-antwort)</li>
	<li>20-30 min pr&auml;sentation, danach ca. 10 min talk (frage-antwort)</li>
	<li>20-30 min pr&auml;sentation, danach ca. 10 min talk (frage-antwort)</li>
	<li>final comments / diskussion / etc.</li>
</ul>
</p>
<p class="text">
jedes meeting dauert also ungef&auml;hr 2.5 stunden und zeigt 3 pr&auml;sentationen.
die k&uuml;nstler bringen soweit alle zur ihrer pr&auml;sentation notwendigen mittel selbst mit.
allerdings stehen bei jedem meeting den pr&auml;sentierenden folgende tools zur verf&uuml;gung:
</p>
<p class="text">
<span class="title">meeting-equipment:</span>
<ul class="list">
	<li>1 g4 powerbook osx</li>
	<li>1 video-projector</li>
	<li>eine schnelle internet-verbindung</li>
	<li>ein mixer und 2 aktiv-monitor lautsprecher</li>
</ul>
</p>
