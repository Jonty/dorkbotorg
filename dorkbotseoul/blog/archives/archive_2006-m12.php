		<p>
Hello,<br />
Dorkbot Seoul is a group that is interested in using electronics and new media to make art bots or DIY projects in Seoul, Korea.<br />
The group is pretty small at this point, however you are welcome to join our mailing list and we would like to have your opinion, thoughts or contributions.<br />
The announce and blabber lists are set up, so you can subscribe at:<br />
<br />
<a href="http://dorkbot.org/mailman/listinfo/dorkbotseoul-announce"  title="announcements" rel='external'>announce mailing list</a><br />
<a href="http://dorkbot.org/mailman/listinfo/dorkbotseoul-blabber"  title="General discussion" rel='external'>blabber mailing list</a><br />
<br />
Hopefully we can see you in our mailing list! Thanks!</p> 
		
		

