<div><p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><b>the purpose of dorkbot-atl is to:</b><o:p></o:p></p>
<ul type="disc">
<li class="MsoNormal" style="mso-pagination:none; mso-list:l1 level1 lfo1; tab-stops:list .5in; mso-layout-grid-align:none; text-autospace:none;">give artists/programmers/engineers an opportunity for
     informal peer review<o:p></o:p></li>
<li class="MsoNormal" style="mso-pagination:none; mso-list:l1 level1 lfo1; tab-stops:list .5in; mso-layout-grid-align:none; text-autospace:none;">establish a forum for the presentation of new art works/technology/software/hardware<o:p></o:p></li>
<li class="MsoNormal" style="mso-pagination:none; mso-list:l1 level1 lfo1; tab-stops:list .5in; mso-layout-grid-align:none; text-autospace:none;">help establish relationships and foster collaboration
     between people with various backgrounds and interests<o:p></o:p></li>
<li class="MsoNormal" style="mso-pagination:none; mso-list:l1 level1 lfo1; tab-stops:list .5in; mso-layout-grid-align:none; text-autospace:none;">give us all a chance to see the cool things that our
     neighbors are working on</li>
</ul>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;">dorkbot tries to be totally neutral with respect to
style and aesthetic. an emphasis on the creative use of electricity is all that
is required. dorkbot also tries to be more informal than conventional academic
forums: no powerpoint slides are required, no academic affiliation is
necessary, and lots of open discussion is encouraged!<o:p></o:p></p>
<ul type="disc">
<li class="MsoNormal" style="mso-pagination:none; mso-list:l0 level1 lfo2; tab-stops:list .5in; mso-layout-grid-align:none; text-autospace:none;">imaginary presentation topics:<o:p></o:p></li>
<li class="MsoNormal" style="mso-pagination:none; mso-list:l0 level1 lfo2; tab-stops:list .5in; mso-layout-grid-align:none; text-autospace:none;">a demo of the new motion tracking software you're
     writing<o:p></o:p></li>
<li class="MsoNormal" style="mso-pagination:none; mso-list:l0 level1 lfo2; tab-stops:list .5in; mso-layout-grid-align:none; text-autospace:none;">playing a video you made<o:p></o:p></li>
<li class="MsoNormal" style="mso-pagination:none; mso-list:l0 level1 lfo2; tab-stops:list .5in; mso-layout-grid-align:none; text-autospace:none;">an explanation of how you hacked your game boy to get
     it to make cool bleeping noises<o:p></o:p></li>
<li class="MsoNormal" style="mso-pagination:none; mso-list:l0 level1 lfo2; tab-stops:list .5in; mso-layout-grid-align:none; text-autospace:none;">trouble shooting/brainstorming your remote-controlled
     sandwich making robot<o:p></o:p></li>
<li class="MsoNormal" style="mso-pagination:none; mso-list:l0 level1 lfo2; tab-stops:list .5in; mso-layout-grid-align:none; text-autospace:none;">bringing in some dancers you're working with and
     having them demonstrate the interface used in your new mind-control ballet</li>
<li class="MsoNormal" style="mso-pagination:none; mso-list:l0 level1 lfo2; tab-stops:list .5in; mso-layout-grid-align:none; text-autospace:none;">discussing your approach to electronic sculpture</li>
<li class="MsoNormal" style="mso-pagination:none; mso-list:l0 level1 lfo2; tab-stops:list .5in; mso-layout-grid-align:none; text-autospace:none;">demos
of commercial software are not appropriate, unless you wrote the software or
you are making it do something particularly novel or interesting. on the other
hand, presenting a piece that was created using commercial software is fine.</li>
</ul>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><b>dorkbot-atl meetings are free and open to the public.</b></p>
