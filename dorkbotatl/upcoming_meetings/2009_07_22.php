<h3>Al Matthews<br>
Blender visualizations for audio by Darren Nelson</h3>

<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;"><a href=""><span style="font-weight: normal;"></span></a><span style="font-weight: normal;"> 

<a href="http://fatmilk.tv/">Al Matthews</a>  will be presenting six visualizations in Blender of audio by <a href="http://www.curiomusic.com/">Darren Nelsen</a>. They were first shown at <a href="http://www.pd.org/~eyedrum/calendar/index.php?eventTypeId=2&id=2811&month=3&year=2009">"less is more (more or less),"</a> a night of Minimalist music at <a href="http://eyedrum.org/">Eyedrum</a>, Atlanta, GA, on 20 March 2009.
</span></p>

<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;"><a href=""><span style="font-weight: normal;"></span></a><span style="font-weight: normal;"> 

Al is a digital media artist in Atlanta, GA.
</span></p>


<h3>Matt Gilbert<br>
GIS visualizations of Atlanta's champion tree population.</h3>

<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;"><a href=""><span style="font-weight: normal;"></span></a><span style="font-weight: normal;"> 

<a href="http://www.mattgilbert.net">Matt Gilbert</a> will present a collaboration (in progress) with <a href="http://dogsfirebeerjournal.blogspot.com/">Eli Dickerson</a> and <a href="http://www.treesatlanta.org/">Trees Atlanta</a> to log and measure Atlanta's champion/old growth trees, and create a website with embedded GIS visualizations of these populations.
</span></p>


<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;"><a href=""><span style="font-weight: normal;"></span></a><span style="font-weight: normal;"> 

Matt began working with local organizer and tree nerd <a href="http://dogsfirebeerjournal.blogspot.com/">Eli Dickerson</a> to find, measure, and map old growth trees around the city using GPS and GIS software like Google Earth. We've recently joined forces with <a href="http://www.treesatlanta.org/">Trees Atlanta</a> who, among many other great projects, compiles an annual <a href="http://www.treesatlanta.org/AtlantasBiggestTrees.aspx">Champion Tree List</a> out of submissions from Atlanta residents. 
</span></p>

