<td width="792" style="width:11.0in; border:solid gray 1.0pt; border-left:none; mso-border-left-alt:solid gray 1.0pt; padding:0in 5.4pt 0in 5.4pt;"><a href="http://danielbauen.com/robotany/">Robotany</a> is a collaborative of Jill Coffin, John Taylor, and Daniel Bauen to combine nature and robotics. Breeze, the first installation of Robotany, is an ambient robot inhabiting the body of a japanese maple. Breeze can visually sense and react through 360 degrees, allowing her to reach out to you and others whenever you are near. This is not a dancing bush, the motion is subtle and artistic, and at the same time, surreal.<br />
<br />
<b>
Daniel Bauen</b> (Lives in Atlanta, GA) is a Swiss-American mechanical engineer focused in the design and application of electromechanical systems (mechatronics): combining electronics, programming, and mechanics to work on projects involving robotics, product design, interactive art, micro-controllers, and biomechanics. He currently works on monumental, interactive stone and steel sculptures, and as a mechanical engineering consultant for Function Engineering.<br />
<br />
<b>
Jill Coffin</b>  (Lives in Zurich, Switzerland) is a digital artist, wearable electronics designer, and doctoral student at the Wearable Computing Lab, Institut fuer Elektronik, ETH Zuerich.<br />
<br />
<b>
John Taylor</b> (Lives in Seattle, Washington) is a nomadic technologist, originally from the Silicon Valley.  The bulk of his belongings are currently on Bainbridge Island in Washington State, but cities such as Portland, Los Angeles,  New York, Austin, Zurich, and San Francisco have recently played host  to his wanderlust.  Currently, his travels are distracting him from
finishing his thesis for a Masters of Science in Architecture from  Cal Poly, SLO.  Other monkeys-on-his-back include contract event production for Red Bull, experimenting with multi-camera panoramic HD video, and the occasional game of Capoeira.<br />
