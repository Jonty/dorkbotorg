<td width="792" style="width:11.0in; border-top:none; border-left:none; border-bottom:solid gray 1.0pt; border-right:solid gray 1.0pt; mso-border-top-alt:
  solid gray 1.0pt; mso-border-left-alt:solid gray 1.0pt; padding:0in 5.4pt 0in 5.4pt;"><a href="http://www-static.cc.gatech.edu/~gilwein/Haile.htm">Haile</a>, a robotic drummer that can listen and interact with other human players in a drum circle will be demonstrated and explained.&nbsp; Haile uses two robotic arms to play an American Indian PowWow drum along with human players by listening to audio from microphones installed in the humans' drums.&nbsp; The talk will go over some of the ways it interacts with people, along with some of the nuts-and-bolts of how it operates.&nbsp;<br class="webkit-block-placeholder" />
<br class="webkit-block-placeholder" />
<a href="http://www.curiousinventor.com/">Scott Driscoll</a> has been working on Haile as his primary project while a grad student at the Music Technology department for the last year and a half.&nbsp; Before joining the music department, he completed a mechanical engineering undergrad at Carnegie Mellon with minors in computer science and robotics, and then a mechanical masters degree at GaTech.&nbsp; He is currently taking part in the development of an amateur robotics company, and plays piano and darbuka on the side.
