  none; text-underline:none;">Moving Light Environments</span></a><o:p></o:p></p>
<p class="MsoNormal" align="center" style="text-align: center;">&nbsp;<o:p></o:p></p>
</td>
<td width="815" style="width:815.0pt; border:solid gray 1.0pt; border-left:none; mso-border-left-alt:solid gray 1.0pt; padding:0in 5.4pt 0in 5.4pt;"><p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:none; text-autospace:none;"><a href="http://www.gregkellum.com/"><span style="text-decoration:none; text-underline:none;">Greg Kellum</span></a>
  will discuss how to use Max to control lights and create moving light
  environments. A brief description of the hardware with which one can control
  lights using software as well as an overview of useful objects in Max for
  conceptualizing and controlling movements of light. Plus, an introduction to
  one such system developed in Max by Greg Kellum for controlling matrices of
  light bulbs with a p5 virtual reality glove.<o:p></o:p></p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:none; text-autospace:none;">&nbsp;<o:p></o:p></p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:none; text-autospace:none;">Greg Kellum (born 1977) began learning about electronic
  music while working on a play whose last two acts he wanted to set to music.
  He studied music (among other things) at Columbia University, and since then,
  he has created music and/or electronic instruments for a number of music
  theater productions including Schiller's Mary Stuart and Artaud's Letter from
  Rodez (1945). He is currently working on a full length album, but when he is
  avoiding working on music, he also writes music software and develops ideas
  for live electronic performances.<o:p></o:p></p>
