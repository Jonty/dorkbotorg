<div><p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><span><b>Thursday, January 12th, 2006 at 7 p.m.</b></span><span><b><o:p></o:p></b></span></p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><span><b>Georgia Tech Music Department (Couch Building)</b></span></p>
<table border="1" cellspacing="0" cellpadding="0" style="mso-table-layout-alt:fixed; border:none; mso-border-top-alt:solid gray 1.0pt; mso-border-left-alt:solid gray 1.0pt; mso-border-right-alt:solid gray 1.0pt; mso-padding-alt:0in 5.4pt 0in 5.4pt;">
