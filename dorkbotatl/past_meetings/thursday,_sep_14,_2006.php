<td width="792" style="width:11.0in; border:solid gray 1.0pt; border-left:none; mso-border-left-alt:solid gray 1.0pt; padding:0in 5.4pt 0in 5.4pt;"><span style="-webkit-border-horizontal-spacing: 0px; -webkit-border-vertical-spacing: 0px;"><p class="MsoNormal">Danielle will discuss her experimentations in ultrasonic directional sound, public intervention and the role art and technology in globalization. Through her project, <a href="http://www.globalportals.org">Global Portals</a>, public realm and virtual space create a more tangible cultural community and an open exchange of critical dialogue.<span style="mso-spacerun: yes;">&nbsp; </span>Danielle will talk briefly about upcoming intercontinental studio sessions with digital sound artists in New York, Beijing and South Africa.<o:p></o:p></p>
<p class="MsoNormal"><a href="http://www.danielleroney.com">Danielle Roney</a> is an installation artist examining globalization through sculpture, digital media and public intervention. After studying at the University of Georgia in Sculpture and Digital Media, she has held studios in Los Angeles, Atlanta and most recently Beijing, China. Her work with technology includes video, ultrasonic sound emitters, and development of public realm simulcasting events.</p>
<p class="MsoNormal">Beginning a new phase of international public art, Danielle has presented her Global Portals project to TEDGlobal 2005 in Oxford, England, highlighting worldwide distribution of her audio-visual communication portals to culminate with the Beijing Olympics in 2008. She attended a two-month residency in Beijing, China while showing in the Beijing Off-Biennale 2005 exhibition, Convergence, curated by Marilyn Kiang and Feng Boyi.</p>
<p class="MsoNormal">Her current exhibition features video refraction sequences and a sculpture/performance video compilation in Genesis Trial, for the Atlanta Contemporary Art Center, while preparing for an intercontinental simulcast between Atlanta, Georgia, Durban, South Africa and Beijing, China January 2007. She returns to another TEDGlobal 2007 in Arusha, Tanzania to discuss interactivity as a part of African redevelopment. Danielle Roney is currently featured in the top 50 emerging international artists by Contemporary magazine, London; highlighting her Global Portals project.</p>
