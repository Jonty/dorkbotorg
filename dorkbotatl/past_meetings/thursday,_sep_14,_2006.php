<p class="MsoNormal" style="mso-pagination:
none; mso-layout-grid-align:none; text-autospace:none;">Microcontollers provide a
great way for technological artists to&nbsp;provide their work with complex and
interactive behaviors using a&nbsp;compact system.&nbsp; What microcontrollers
are, why you might want to use&nbsp;them rather than PCs, and the different
kinds of microcontrollers will&nbsp;be discussed.<o:p></o:p></p>
<p class="MsoNormal" style="mso-pagination:
none; mso-layout-grid-align:none; text-autospace:none;">&nbsp;<o:p></o:p></p>
<p class="MsoNormal" style="mso-pagination:
none; mso-layout-grid-align:none; text-autospace:none;"><a href="http://www.t11s.com/"><span style="text-decoration:none; text-underline:none;">Thomas Edwards</span></a> is a technological artist based
in the Washington, DC&nbsp;area.&nbsp; He has a background in electrical
engineering, and is a&nbsp;practicing broadcast engineer.&nbsp; Edwards' work
concentrates on&nbsp;interactions between humans and robotic systems and
telepresence. Many of his robotic works deal with the "uncanny
valley" where robotic&nbsp;systems have both the qualities of robots as
well as humans.<o:p></o:p></p>
