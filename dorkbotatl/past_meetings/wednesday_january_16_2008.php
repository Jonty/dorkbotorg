<div><p><span style="color: rgb(0, 0, 0); font-family: Times; font-size: 16px;"></span></p>
<p class="MsoNormal"><b><span style="color: rgb(0, 0, 0); font-family: Times; font-size: 16px; font-weight: normal;"></span></b></p>
<b><p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; color: rgb(17, 17, 17); font-family: Georgia; font-size: 12px;">7 p.m. at the Georgia Tech Music Department (Couch Building 207/209)</p>
<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; color: rgb(17, 17, 17); font-family: Georgia; font-size: 12px;"><br style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;" />
</p>
<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; color: rgb(17, 17, 17); font-family: Georgia; font-size: 12px;">Directions are <a href="applewebdata://FFDEDCCF-0317-4F0C-9E3B-FC93D12B0CD4/../directions.php" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;">here</a>.</p>
<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; color: rgb(17, 17, 17); font-family: Georgia; font-size: 12px;"><br class="webkit-block-placeholder" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;" />
</p>
<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; color: rgb(17, 17, 17); font-family: Georgia; font-size: 12px;">
</p>
<div>Nick Didkovsky:&nbsp;MetaSection Piece Generator, a program that composes music for live ensemble</div>
<div><br class="webkit-block-placeholder" />
</div>
<div><span style="font-weight: normal;">Nick will discuss and demonstrate his unique approach to algorithmic composition, joining us via videoconference from New York.</span></div>
<div><br />
</div>
<div><span style="font-weight: normal;">Nick Didkovsky is a guitarist, composer, and software programmer. In 1983, he founded the avant-rock septet Doctor Nerve. He presently resides in New York City, where he composes, creates music software, and teaches computer music composition. He is the principle author of the computer music language <a href="http://www.algomusic.com" target="_blank">Java Music Specification Language</a>&nbsp;and uses it to teach at New York University and Columbia University. He is director of bioinformatics for the Gensat project at The Rockefeller University.</span></div>
<div><span style="font-weight: normal;"><br class="webkit-block-placeholder" />
</span></div>
<div><span style="font-weight: normal;">Didkovsky’s work with <a href="http://www.doctornerve.org" target="_blank">Doctor Nerve</a>&nbsp;joins the furious energy of rock with intricate composition, some of which finds its origins in rich software systems of his own design. His non-didactic approach to combining human and machine creativity is his unique fingerprint in a musical world that pushes the boundaries of rock music, algorithmic composition, and contemporary music.</span></div>
<div><span style="font-weight: normal;"><br class="webkit-block-placeholder" />
</span></div>
<div><span style="font-weight: normal;">Didkovsky has also composed music for Bang On A Can All-Stars, Meridian Arts Ensemble, Fred Frith Guitar Quartet, California EAR Unit, New Century Players, Either/Or, ARTE Quartett, Downtown Ensemble, and others. His works are available on CD and online; recent releases include “Ice Cream Time” for ARTE Sax Quartett, Tom Dimuzio, and Didkovsky (New World Records 80667), “Tube Mouth Bow String, music for electric guitar, computer, live electronics, and string quartet” (Pogus Productions 21042-2), and “Swim This – Gerry Hemingway, Michael Lytle, and Nick Didkovsky”, released on his own Punos Music label.</span></div>
<div><span style="font-weight: normal;"><br class="webkit-block-placeholder" />
</span></div>
<div><span style="font-weight: normal;">With Phil Burk, Didkovsky created JMSL (Java Music Specification Language), a language for computer music composition written in the Java programming language. JMSL was premiered at Circuits: The Governor’s Conference on Arts and Technology, in Palisades, NY in March, 1998. In 2003, Minnesota Public Radio commissioned a JMSL version of Henry Cowell &amp; Leon Theremin’s historical electronic music instrument, the Rhythmicon (1930), which can be performed at <a href="http://www.musicmavericks.org/rhythmicon" target="_blank">www.musicmavericks.org/rhythmicon</a>. Didkovsky recently premiered a new composition for four tabletop electric guitarists following a real-time score generated by JMSL, at the Music in the Global Village conference in Budapest, which featured Keith Rowe, Hans Tammen, Erhard Hirt, and Didkovsky.</span></div>
<div><span style="font-weight: normal;"><br class="webkit-block-placeholder" />
</span></div>
<div><span style="font-weight: normal;">His online interactive musical works are available at <a href="http://www.punosmusic.com" target="_blank">www.punosmusic.com</a> . Pieces include “Music for Hot Spots” where the user is instructed to “Put on your headphones, turn on your laptop's mic, and listen. Music for Hotspots dramatically alters the sound of your environment.”</span></div>
<div><span style="font-weight: normal;"><br class="webkit-block-placeholder" />
</span></div>
<div><span style="font-weight: normal;">On February 12th, Georgia Tech's ensemble-in-residence, <a href="http://www.sonicgenerator.gatech.edu/" target="_blank">Sonic Generator</a>, will perform Didkovsky's <i>Rain on a Frail Cutie.</i></span></div>
