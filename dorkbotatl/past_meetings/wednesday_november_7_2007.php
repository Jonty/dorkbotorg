<div>Jason Freeman will present an informal performance of <a href="http://www.jasonfreeman.net/flock/" target="_blank">Flock</a>&nbsp;(for saxophone quartet, dancers, electronic sound, video, and audience participation) in preparation for the work's premiere at the Carnival Center for the Performing Arts in Miami in December. Flock is being developed by Freeman in collaboration with&nbsp;Mark Godfrey,&nbsp;Liubo Borissov, Frank Dellaert, Dan Hou, Justin Berger, and Martin Robinson. Come and help create the music being performed by a live saxophone quartet and learn how everything works. The project has evolved considerably since last May's &quot;test&quot; presentation at Dorkbot; your feedback at that time was critical in pushing development of the work forward.</div>
<div><br class="webkit-block-placeholder" />
</div>
<div>Jason Freeman’s works break down conventional barriers between composers, performers, and listeners, using cutting-edge technology and unconventional notation to turn audiences and musicians into compositional collaborators. His music has been performed by the American Composers Orchestra, Speculum Musicae, the So Percussion Group, the Nieuw Ensemble, Le Nouvel Ensemble Moderne, and Evan Ziporyn; and his interactive installations and software art have been exhibited at the Lincoln Center Festival, the Boston CyberArt Festival, and the Transmediale Festival and featured in the New York Times and on National Public Radio. N.A.G. (Network Auralization for Gnutella) (2003), a commission from Turbulence.org, was described by Billboard as “…an example of the web’s mind-expanding possibilities.” Freeman received his B.A. in music from Yale University and his M.A. and D.M.A. in composition from Columbia University. In 2005, he joined the faculty at the Georgia Institute of Technology, where he is an assistant professor in the music department.</div>
