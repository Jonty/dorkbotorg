<p class="MsoNormal" align="center"><b>Blind Source Separation in Music</b></p>
<img src="../_Media/mitch-small-100x100.png" class="wide" alt="mitch-small-100x100" />
</td>
<td width="792" style="width:11.0in; border-top:none; border-left:none; border-bottom:solid gray 1.0pt; border-right:solid gray 1.0pt; mso-border-top-alt:
  solid gray 1.0pt; mso-border-left-alt:solid gray 1.0pt; padding:0in 5.4pt 0in 5.4pt;"><p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:none; text-autospace:none;">In this talk, I will present my work on sound source separation with
applications for music.  Music is repetitious in nature and this
repetition actually informs the source separation process.  I derive an
automated statistical approach based entirely on repetitive structure to
separate sound sources.  In addition, spectrograms contain time-frequency
structure.  This structure may be factored into note-like components
containing a spectral shape modulated by an amplitude envelope.  When
multiple spectrograms are available, I show how to incorporate this
additional spatial information to separate components and combine them to
form the original source signals.</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:none; text-autospace:none;"><a href="http://home.cc.gatech.edu/parry" target="_blank">Mitchell Parry</a> is a Ph.D. candidate in the College of Computing working
with Irfan Essa.  His research interests include source separation, signal
processing, visualization and music information retrieval.  He majored in
computer science with a minor in electrical engineering at the University
of Virginia before coming to Georgia Tech where he received his masters in
computer science with a specialization in computer graphics and
visualization.&nbsp;<br />
</p>
