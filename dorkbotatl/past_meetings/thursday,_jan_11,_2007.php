<td width="792" style="width:11.0in; border:solid gray 1.0pt; border-left:none; mso-border-left-alt:solid gray 1.0pt; padding:0in 5.4pt 0in 5.4pt;">Some recent audio/video projects that use generative processes to yield
surprising results. "Temporide" does a pixel-by-pixel delay on a video,
showing many time lapses simultaneously. Spectral splicing, morphing, and
reconstitution creates new audio based out of what you feed it. And "Ghost
Jockey" generates a continuous stream of mashup audio and video.<br />
<br />
<a href="http://music.columbia.edu/~daniglesia" target="_blank">Daniel Iglesia</a> makes electronic music and video, or more accurately, is lazy
and creates machines that do it for him. Much of his work has automatic/ generative/ algorithmic processes to emulate human creativity, or
to aid him in an improvisational setting. He does concert music, A/V
installations and performance systems, network art projects, pop music,
collaborations with other disciplines, and almost anything else. He is a
Teaching Fellow at Columbia's Computer Music Center. Recent appearances at
Rose Studio at Lincoln Center, The Stone, Eyebeam Gallery,
Ontological-Hysteric Theatre, Merce Cunningham Studio, IMEB (Bourges), CCMIX
(Paris), the Delancey, and many more.&nbsp;<br />
