<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;">Directions are <a href="../directions.php">here</a>.</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;">(Note that we're in the Rehearsal Hall on the first floor of Couch this month, not in our regular room on the second floor.)</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><br />
</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><a href="http://www.gratkowski.com/">Frank Gratkowski</a> will be giving a presentation / demonstration titled "<span style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; text-align: center;">Articulations: Music for saxophones, clarinets, and technology." </span>
Gratkowski has given solo performances since 1990. With his solo program, he was a 1991 prizewinner in the Musik Kreativ contest. The release of the solo CD "Artikulationen" followed the same year. A new one "Artikulatioenen II" has been released in 2002. Since then he has continuously developed his solo work and has performed in various venues (jazz festivals, clubs, contemporary music festivals, art museums, art galleries, etc). His music has been developed on the basis of improvisation, and it makes use of various resources. Formal processes, tone material and sound generation are used to create structural architecture, so that every piece has its own specific character.&nbsp; One main component of the concept is the experimental exploration of the Instrument.&nbsp; With his solo music, he has also looked for cooperation or meetings with other art forms such as painting, literature, sculpture, dance, film and multimedia projects. Since 1999 he's doing extended solo tours in the USA and Canada once a year.</p>
