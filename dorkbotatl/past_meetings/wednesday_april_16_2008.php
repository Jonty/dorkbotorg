</p>
<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; color: rgb(17, 17, 17); font-family: Georgia; font-size: 12px;"><span style="font-weight: normal;">The final dorkbot meeting of the academic year features composer and sound artist Christopher Bailey, who joins us via live videoconference, and Andrew Beck, a masters candidate in music technology at Georgia Tech.</span></p>
<div><span style="font-weight: normal;">--
</span></div>
<div><div><span style="font-weight: normal;">Born outside of Philadelphia, PA, Christopher Bailey turned to music composition in his late teens, and to electroacoustic composition during his studies at the Eastman School of Music, and later at Columbia University. Recent performances of his music occurred in Taiwan, Germany, Montreal, New York, Chicago, Miami, New Orleans, Houston, Minneapolis, and in Seoul, Korea, where he was a 2nd-Prize recipient in the International Composers Competition. Other awards include prizes from BMI and ASCAP, and the Bearns Prize. He has recently released a CD of piano music with electronics.</span></div>
<div><span style="font-weight: normal;"><br />
</span></div>
<div><span style="font-weight: normal;"><span style="color: rgb(17, 17, 17); font-family: Georgia; font-size: 12px; font-style: normal; font-weight: normal; white-space: normal;">As part of a recent residency at Harvestworks in New York, Bailey&nbsp;created a database system for organizing musique concrete. The database system allows one to catalogue sounds according to a number of musical parameters. Imagining abstract musical gestures, one can then use the system to realize the gestures with different combinations of found sounds.</span><br />
</span></div>
<div><span style="font-weight: normal;">--
</span></div>
<div><span style="font-weight: normal;"><div>Andrew Beck presents a trial run of Free Field, to be premiered at the&nbsp;2008 <a href="http://lm.gatech.edu" target="_blank">Listening Machines</a> Concert in the Eyedrum on April 24th:</div>
<div><br />
</div>
<div>The spaces we live in inform our experience. We project our view of&nbsp;the world into every room we live in, forming the silent backdrops to&nbsp;our everyday lives. Given modern technology, every action we make is&nbsp;recorded and archived somewhere, probably never to be seen by human</div>
<div>eyes again. We are gradually becoming accustomed to the data we&nbsp;generate and allow it to happen behind the scenes. What happens if we&nbsp;were able to hear the bits and pieces of information we leave behind?&nbsp;Free Field is a playful exploration of these themes, picking up pieces&nbsp;of people's conversation and noises to play back in unique ways. Every&nbsp;sound that happens within its walls is recorded and analyzed, allowing&nbsp;participants to interact with the system in unexpected ways.</div>
