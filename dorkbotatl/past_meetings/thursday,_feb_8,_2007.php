<div><p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><b>7 p.m. at the <span style="color: rgb(255, 0, 151);">Skiles Courtyard Gallery</span> (special location)</b></p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><br class="webkit-block-placeholder" />
</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;">Directions to the Skiles building are available from <span style="color: rgb(0, 0, 0); font-family: Times; font-size: 16px;"><a href="http://maps.google.com/maps?f=q&amp;hl=en&amp;q=686 Cherry St, atlanta, ga 30332&amp;ie=UTF8&amp;z=15&amp;ll=33.773618,-84.395599&amp;spn=0.018122,0.0418&amp;om=1&amp;iwloc=addr" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; text-decoration: none; font-family: Georgia; font-size: 12px;"><b>Google maps</b></a></span>. Visitor parking is available in the <span style="color: rgb(0, 0, 0); font-family: Times; font-size: 16px;"><a href="http://www.parking.gatech.edu/parking/1_on_campus_parking/1_visitor_parking/" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; text-decoration: none; font-family: Georgia; font-size: 12px;"><b>student center parking lot</b></a></span> or in metered spaces on the street.<br class="webkit-block-placeholder" />
</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><br class="webkit-block-placeholder" />
</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;">We are teaming up with the Digital Media program at Georgia Tech for a special dorkbot in a special location this month. It is all part of an exhibition of the work that Mary Flanagan, a long-time friend of dorkbot, has created during her residency at Georgia Tech.</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><br class="webkit-block-placeholder" />
</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><b>Full Schedule:</b></p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;">4:30 pm: Mary Flanagan lecture in Skiles 002</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;">6:00-8:00 pm: Exhibition Reception, Skiles Courtyard Gallery</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;">7:00 pm: Dorkbot meeting featuring a hands-on presentation by Mary, Skiles Courtyard Gallery</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><br />
</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><span style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; color: rgb(0, 0, 0); font-family: Times; font-size: 16px;"><a href="http://maryflanagan.com/" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; text-decoration: none; font-family: Georgia; font-size: 12px;"><b>Mary Flanagan</b></a></span> investigates everyday technologies through critical writing, artwork, and activist design projects. Flanagan's work has been exhibited internationally at museums, festivals, and galleries, including: the Guggenheim, The Whitney Museum of American Art, SIGGRAPH, The Banff Centre, The Moving Image Centre, New Zealand, Central Fine Arts Gallery NY, Artists Space NY, the University of Arizona, University of Colorado-Boulder, and venues in Brazil, France, UK, Canada, Taiwan, New Zealand, and Australia. Her projects have been funded by the National Science Foundation, the Pacific Cultural Foundation, and the National Endowment for the Arts.<br />
</p>
<div><br class="webkit-block-placeholder" />
</div>
<div>Flanagan writes about popular culture and digital media such as computer games, virtual agents, and online spaces in order to understand their affect on culture. Her essays on digital art, cyberculture, and gaming have appeared in periodicals such as Art Journal , Wide Angle , Intelligent Agent, Convergence, and Culture Machine, as well as several books. Her co-edited collection reload: rethinking women + cyberculture with Austin Booth was published by MIT Press in 2002. She is also co-author with Matteo Bittanti of Similitudini. Simboli. Simulacri ( SIMilarities, Symbols, Simulacra ) on The Sims game (in Italian, Unicopli 2003), and the co-editor of the collection re:skin , forthcoming from MIT Press.</div>
<div><br class="webkit-block-placeholder" />
</div>
<div>Flanagan is also the creator of &quot;The Adventures of Josie True,&quot; the first web-based adventure game for girls, and is implementing innovations in pedagogical and values-based game design.</div>
<div><br class="webkit-block-placeholder" />
</div>
<div>Mary Flanagan holds MFA and MA degrees from the University of Iowa, a BA in Film from the University of Wisconsin-Milwaukee, and a Ph.D. in Computational Media focusing on activist game design from Central Saint Martins College of Art and Design, UK. She teaches in the Integrated Media Arts MFA program in the Department of Film and Media Studies at Hunter College, NYC. Her research group and laboratory in New York is called TiltFactor, a lab focused on the design of activists and socially-conscious software. http://www.tiltfactor.org.</div>
