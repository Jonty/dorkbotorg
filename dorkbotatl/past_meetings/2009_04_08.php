<h3>Travis Thatcher's reading list</h3>

<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;"><a href="http://curiomusic.com"><span style="font-weight: normal;"></span></a><span style="font-weight: normal;"> The Art of Electronics - <a href="http://www.amazon.com/Art-Electronics-Paul-Horowitz/dp/0521370957/ref=sr_1_1?ie=UTF8&s=books&qid=1239374536&sr=8-1">Amazon link</a>. Great reference for all sorts of analog and digital circuits, and some of the theory behind them.</span></p>

<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;"><a href="http://curiomusic.com"><span style="font-weight: normal;"></span></a><span style="font-weight: normal;"> Electronic Projects for Musicians - <a href="http://www.amazon.com/Electronic-Projects-Musicians-Craig-Anderton/dp/0825695023/ref=pd_bxgy_b_img_c">Amazon link</a>. Awesome book by Craig Anderton with tons of simple guitar pedal circuits.  Great for beginners.</span></p>

<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;"><a href="http://curiomusic.com"><span style="font-weight: normal;"></span></a><span style="font-weight: normal;"> Handmade Electronic Music - By Nicolas Collins. Another good resource that starts simple and covers everything from circuit bending, to simple circuits, to building new instruments with sensors.  Another great one for beginners.</span></p>


<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;"><a href="http://curiomusic.com"><span style="font-weight: normal;"></span></a><span style="font-weight: normal;"> The Engineer's Mini-Notebook 555 Timer IC Circuits - <a href="http://www.amazon.com/Engineers-Mini-Notebook-Timer-Circuits-276-5010/dp/B000MN54A6/ref=sr_1_2?ie=UTF8&s=books&qid=1239375106&sr=1-2">Amazon link</a>. Lots of simple circuits based around the 555 timer chip.</span></p>


