<h3>Beth Milliken<br>
Freeside Atlanta non-profit Hackerspace</h3>

<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;"><a href="http://curiomusic.com"><span style="font-weight: normal;"></span></a><span style="font-weight: normal;"> 

<a href="http://wiki.freesideatlanta.org/">Freeside</a> is a new Atlanta based non-profit Hackerspace - encouraging collaboration and innovation across hardware and software disciplines without the constraints of corporations or academia.
</span></p>


<h3>Travis Thatcher/Scott Driscoll<br>
The Voice of Saturn, DIY modular synthesizer and CuriousInventor.com</h3>
<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;"><a href="http://curiomusic.com"><span style="font-weight: normal;"></span></a><span style="font-weight: normal;"> 

Travis (<a href="http://www.recompas.com">recompas</a>) will demo and explain the ideas behind his <a href="http://voiceofsaturn.blogspot.com">Voice of Saturn</a> synthesizer system, which has been released and supported by Scott Driscoll of <a href="http://www.curiousinventor.com">Curious Inventor</a>. Travis will also discuss general diy practices and tips on getting into diy electronics.
</span></p>
<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;"><a href="http://curiomusic.com"><span style="font-weight: normal;"></span></a><span style="font-weight: normal;"> 

Scott will demo a lighted touch controller called Stribe, which can be used for anything from a touchable VU meter to a scrubbing sampler.  All the software is written in max/msp and much of the hardware is supported by an Arduino board. 
</span></p>
<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;"><a href="http://curiomusic.com"><span style="font-weight: normal;"></span></a><span style="font-weight: normal;"> 

<b>Bios:</b><br>
Travis Thatcher has been active in the Atlanta experimental music scene since arriving in 2000 to attend Georgia Tech.  His interest in electronics and electronic music, particularly dealing with analog synthesizers, lead him to begin experimenting with building his own devices. 
</span></p>
<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;"><a href="http://curiomusic.com"><span style="font-weight: normal;"></span></a><span style="font-weight: normal;"> 

Scott Driscoll runs a DIY kit company called <a href="http://www.curiousinventor.com">CuriousInventor.com</a>.  He went to school at GaTech to study mechanical engineering and music technology, and has spent his life creating an assortment of strange gadgetry, for example, a telnet operated door opener, a robot that plays the drums with people, and a play-station to MIDI box. 
</span></p>

