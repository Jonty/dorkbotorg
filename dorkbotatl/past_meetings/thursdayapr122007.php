<div><p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><b>7 p.m. at the Georgia Tech Music Department (Couch Building, Room 207)</b></p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><b><br />
</b></p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;">Directions are <a href="../directions.php">here</a>.</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><br />
</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;">At this final meeting of the 2006-2007 academic year, Jason Freeman will present a hands-on demonstration of his work-in-progress, Flock. Flock is being developed by Freeman in collaboration with Liubo Borissov, Frank Dellaert, Mark Godfrey, Dan Hou, Justin Berger, and Martin Robinson. Come and help create the music being performed by a live saxophone quartet, learn how everything works, and give us feedback on the experience as we continue to develop the piece.</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><br class="webkit-block-placeholder" />
</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><a href="http://www.jasonfreeman.net/flock/" target="_blank">Flock</a> is a full evening performance work for saxophone quartet, conceived to directly engage audiences in the composition of music by physically bringing them out of their seats and enfolding them into the creative process. During the performance, the four musicians and 60-80 audience members move freely around the performance space. A computer vision system determines the locations of the audience members and musicians, and it uses that data to generate performance instructions for the saxophonists, who view them on wireless handheld displays mounted on their instruments. The data is also artistically rendered and projected on multiple video screens to provide a visual experience of the score. <br class="webkit-block-placeholder" />
</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;"><br class="webkit-block-placeholder" />
</p>
<p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:
none; text-autospace:none;">
</p>
<div><a href="http://www.jasonfreeman.net/" target="_blank">Jason Freeman’s</a> works break down conventional barriers between composers, performers, and listeners, using cutting-edge technology and unconventional notation to turn audiences and musicians into compositional collaborators. His music has been performed by the American Composers Orchestra, Speculum Musicae, the So Percussion Group, the Nieuw Ensemble, Le Nouvel Ensemble Moderne, and Evan Ziporyn; and his interactive installations and software art have been exhibited at the Lincoln Center Festival, the Boston CyberArt Festival, and the Transmediale Festival and featured in the New York Times and on National Public Radio. N.A.G. (Network Auralization for Gnutella) (2003), a commission from Turbulence.org, was described by Billboard as “…an example of the web’s mind-expanding possibilities.” Freeman received his B.A. in music from Yale University and his M.A. and D.M.A. in composition from Columbia University. He recently joined the faculty at the Georgia Institute of Technology, where he is an assistant professor in the music department.</div>
