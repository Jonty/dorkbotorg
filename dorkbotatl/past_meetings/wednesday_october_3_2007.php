<div><p><span style="color: rgb(0, 0, 0); font-family: Times; font-size: 16px;"></span></p>
<p class="MsoNormal"><b><span style="color: rgb(0, 0, 0); font-family: Times; font-size: 16px; font-weight: normal;"></span></b></p>
<b><p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; color: rgb(17, 17, 17); font-family: Georgia; font-size: 12px;">7 p.m. at the Georgia Tech Music Department (Couch Building, room 207)</p>
<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; color: rgb(17, 17, 17); font-family: Georgia; font-size: 12px;"><br style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;" />
</p>
<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; color: rgb(17, 17, 17); font-family: Georgia; font-size: 12px;">Directions are <a href="applewebdata://FFDEDCCF-0317-4F0C-9E3B-FC93D12B0CD4/../directions.php" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;">here</a>.</p>
<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; color: rgb(17, 17, 17); font-family: Georgia; font-size: 12px;"><br class="webkit-block-placeholder" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px;" />
</p>
<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; color: rgb(17, 17, 17); font-family: Georgia; font-size: 12px;">Carla Diana:&nbsp;Crafting Control: The Joy of Interacting in Real Time</p>
<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; color: rgb(17, 17, 17); font-family: Georgia; font-size: 12px;"><br class="webkit-block-placeholder" />
</p>
<p class="MsoNormal" style="padding-top: 0px; padding-right: 0px; padding-bottom: 0px; padding-left: 0px; color: rgb(17, 17, 17); font-family: Georgia; font-size: 12px;">
</p>
<div><span style="font-weight: normal;">Carla Diana is a designer, artist and educator whose work blurs the boundaries between a variety of disciplines, all dealing with creative technology in some way. In this session, she will talk about her experience as an industrial and interaction designer dealing with user experiences that are both digital and physical at the same time. Through a showcase of client work, personal research and art installations, she will demonstrate how ideas and experiments from a variety of seemingly disparate influences can combine to form new solutions that are much richer and deeper than those attained through traditional professional processes alone.&nbsp;</span></div>
<div><br />
</div>
<div><span style="font-weight: normal;"><a href="http://www.carladiana.com" target="_blank">Carla Diana</a> is a creative professional with over 15 years of diverse experience in design and engineering. Her career began conducting product design research at the Good Housekeeping Institute in New York City where she led a team of engineers and researchers who influenced many of the objects we use in our homes today. She went on to study design theory and apply her experience to projects ranging from housewares to consumer electronics, with a focus on interface and innovative interaction design. Carla has worked with some of world's best design professionals at frog design, Sarkissian-Mason, DDB Needham and Karim Rashid's studio, with clients such as HP, Microsoft and Mazda. She is a founding partner in the recently formed design consultancy Spank and the creator of the interactive sound project Repercussion.org, which has been featured in several design publications, art festivals and events throughout the world. She holds an MFA in Design from Cranbrook Academy of Art and a Bachelors in Mechanical Engineering from the Cooper Union.&nbsp;Carla has taught design at several institutions including Parsons School of Design, Savannah College of Art and Design and the Georgia Institute of Technology, where she currently holds the position of Visiting Assistant Professor of Industrial Design at the College of Architecture.</span></div>
<div><span style="font-weight: normal;"><br class="webkit-block-placeholder" />
</span></div>
<div>Claudia Winegarden:&nbsp;The Intrinsic Cross-Disciplinary Aspect of Industrial Design</div>
<div><br class="webkit-block-placeholder" />
</div>
<div><div><span style="font-weight: normal;">Claudia Winegarden will introduce the&nbsp;<a href="http://coa.gatech.edu/id/">industrial design curriculum</a> and talk about the cross-disciplinary&nbsp;nature of the program. She will present models of interaction across&nbsp;disciplines as well as showcasing examples of student work and current&nbsp;projects.</span></div>
<div><span style="font-weight: normal;"><br class="webkit-block-placeholder" />
</span></div>
<div><span style="font-weight: normal;">Claudia Rébola Winegarden completed her&nbsp;Ph.D. degree in Information Design at North Carolina State University.&nbsp;She also holds a Bachelors of Industrial Design from the Universidad</span></div>
<div><span style="font-weight: normal;">Nacional De Cordoba and a Masters of Industrial Design from North&nbsp;Carolina State University. Her focus is on a wide scope of expertise&nbsp;of communication technologies in the broad areas including&nbsp;industrial/product design, human-computer interaction (HCI),&nbsp;information visualization and learning environments. Dr. Winegarden's&nbsp;passion is applying a holistic, innovative and multidisciplinary&nbsp;approach towards design practices that yield the integration of</span></div>
<div><span style="font-weight: normal;">human-centered, seamless non-invasive technologies and the physicality&nbsp;of the product into one unified experience. Her goal is to humanize&nbsp;technology through design with an interest in enriching and augmenting&nbsp;the human experience with the hybrid environment.</span></div>
