  solid gray 1.0pt; mso-border-left-alt:solid gray 1.0pt; padding:0in 5.4pt 0in 5.4pt;"><p class="MsoNormal" style="mso-pagination:none; mso-layout-grid-align:none; text-autospace:none;"><span style="-webkit-border-horizontal-spacing: 0px; -webkit-border-vertical-spacing: 0px;"><span style="-webkit-border-horizontal-spacing: 0px; -webkit-border-vertical-spacing: 0px;">Sequencer404 is a collaborative, networked music&nbsp;system that has evolved from a touch-tone voice&nbsp;connection based system to a fully networked java&nbsp;system.&nbsp; The goals of Sequencer404 are to provide a&nbsp;simple, novel, and fun interface that enables users&nbsp;geographically far from each other to collaborate in&nbsp;musical creation, and to enable users to create&nbsp;content anywhere they please, with the minimum of&nbsp;equipment.&nbsp;<br class="webkit-block-placeholder" />
Travis Thatcher is currently persuing a Masters in&nbsp;Music Technology at Georgia Tech.&nbsp; His research&nbsp;interests include human computer interaction for live&nbsp;performance and interactive sonification, with a&nbsp;concentration on mobile applications for networked&nbsp;collaboration and on controller design. Thatcher has&nbsp;performed as an electronic composer and musician for&nbsp;the last six years and as a saxophonist for the last&nbsp;eleven years. He he received a BS in Computer Science&nbsp;from Georgia Tech in Spring 2005 and has worked&nbsp;extensively in industry, and is currently an adjunct&nbsp;instructor at the Art Institute of Atlanta.<br />
John Goetzinger is a programmer and digital artist&nbsp;from Atlanta, Ga.&nbsp; Currently he is concentrating on&nbsp;projects focusing on using cellular telephones as&nbsp;controllers for other devices or programs.&nbsp; John&nbsp;received his M.S in Information Design and Technology&nbsp;from Georgia Institute of Technology and currently&nbsp;works in Atlanta as a web programmer.<br />
David Jimison is a PhD candidate in the Digital Media&nbsp;program at Georgia Institute of Technology, where he&nbsp;leads a research team in <a href="http://www.mtg.gatech.edu">Mobile Technology</a>. His research interests are in the&nbsp;effects of mobile technology upon the critique of&nbsp;everyday life.</span></span></p>
