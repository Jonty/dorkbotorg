</td>
<td width="792" style="width:11.0in; border:solid gray 1.0pt; border-left:none; mso-border-left-alt:solid gray 1.0pt; padding:0in 5.4pt 0in 5.4pt;"><span style="-webkit-border-horizontal-spacing: 0px; -webkit-border-vertical-spacing: 0px;"><span style="-webkit-border-horizontal-spacing: 0px; -webkit-border-vertical-spacing: 0px;">This Fall, a special topics Electrical and Computer Engineering class called "Theory and Design of Music Synthesizers" is being offered for the second time at Georgia Tech. Electrical Engineering programs, across the country, have become too theoretical. This class was born out of the desire for a class that emphasizes putting theory in practice, also a desire for a class that cut across multiple disciplines. Synthesizers are an ideal subject matter for both goals. The first half of the class examines music synthesis via analog circuitry, such as the "ladder" voltage controlled filter patented by Robert Moog; the second half considers music synthesis via digital signal processing, including modeling the imperfections of real circuits for "virtual analog" implementations. The homework assignments emphasize analyzing the schematics of real synthesizers, not idealized "textbook" problems. The final project consists of designing and constructing - in solder - part of a modular synthesizer.<br class="webkit-block-placeholder" />
As part of this talk, a <a href="http://www.synthtech.com">SynthTech</a> MOTM modular synthesizer built by last semester's class, as well as designs created by that class, will be demonstrated. The equipment that was constructed last semester, and that will be designed and constructed by this semester's class, will eventually reside in the Georgia Tech Music Department for use by music students.</span></span><br />
