		<p><b>more information:</b><br>
			the purpose of dorkbot-atl is to:<br>
			&#x2022; give artists/programmers/engineers an opportunity for informal peer review<br>
			&#x2022; establish a forum for the presentation of new art works/technology/software/hardware<br>
			&#x2022; help establish relationships and foster collaboration between people with various backgrounds and interests<br>
			&#x2022; give us all a chance to see the cool things that our neighbors are working on</p>
		<p>dorkbot tries to be totally neutral with respect to style and aesthetic. an emphasis on the creative use of electricity is all that is required. dorkbot also tries to be more informal than conventional academic forums: no PowerPoint slides are required, no academic affiliation is necessary, and lots of open discussion is encouraged!</p>
		<p>imaginary presentation topics:<br>
			&#x2022; a demo of the new motion tracking software you're writing<br>
			&#x2022; playing a video you made<br>
			&#x2022; an explanation of how you hacked your game boy to get it to make cool bleeping noises<br>
			&#x2022; trouble shooting/brainstorming your remote-controlled sandwich making robot<br>
			&#x2022; bringing in some dancers you're working with and having them demonstrate the interface used in your new mind-control ballet<br>
			&#x2022; discussing your approach to electronic sculpture<br>
			demos of commercial software are not appropriate, unless you wrote the software or you are making it do something particularly novel or interesting. on the other hand, presenting a piece that was created using commercial software is fine.</p>
		<p>dorkbot-atl meetings are free and open to the public.</p>
