		<p>&#x2022; subscribe to our <a href="http://dorkbot.org/mailman/listinfo/dorkbotatl-announce">super-low-volume announcement list</a> to find out when dorkbot-atl meetings are happening.<br>
			&#x2022; subscribe to our <a href="http://music.columbia.edu/mailman/listinfo/dorkbotatl-blabber">unmoderated blabber mailing list</a> to discuss all things related to electricity, strangeness, and dorkbot<br>
