		<p><b>what:</b></p>
		<p><a href="http://dorkbot.org">dorkbot</a> is a monthly meeting of artists (sound/image/movement/whatever), designers, engineers, students and other interested parties who are involved in the creation of electronic art (in the broadest sense of the term). Dorkbot was started in <a href="http://dorkbot.org/dorkbotnyc/">New York</a> in 2000 by <a href="http://music.columbia.edu/%7ejason/">douglas irving repetto</a> and has since spread to over 40 cities worldwide. (See them all at: <a href="http://dorkbot.org">http://dorkbot.org</a>)<br>
		</p>
		<p><b>who:</b><br>
			dorkbot-atl meetings are coordinated by <a href="http://www.jasonfreeman.net">Jason Freeman</a> and hosted by the <a href="http://www.music.gatech.edu">Georgia Tech Music Department</a>. Contact us at dorkbotatl at dorkbot org. (Yes, there's a dot between dorkbot and org, but we want to fool those silly spam robots.)</p>
